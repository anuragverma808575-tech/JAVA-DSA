package Loops;

public class VowelsandConsonants {

    public static void main(String[] args) {
        char ch = 'b';
        if (ch == 'a') {
            System.out.println("Vowel");
        } else if (ch == 'e') {
            System.out.println("Vowel");
        } else if (ch == 'i') {
            System.out.println("Vowel");
        } else if (ch == 'o') {
            System.out.println("Vowel");
        } else if (ch == 'u') {
            System.out.println("Vowel");
        } else {
            System.out.println("Consonant");
        }
    }

}
